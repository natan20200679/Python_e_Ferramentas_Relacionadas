"""# Two Sum Pairs With 0 Sum"""

def getPairs(arr):
        pairs = set()
        for i in range(len(arr)):
            for j in range(i + 1, len(arr)):
                if arr[i] + arr[j] == 0:
                    if arr[i] < arr[j]:
                      pairs.add((arr[i], arr[j]))
                    else:
                      pairs.add((arr[j], arr[i]))
                else:
                  continue
        return sorted(list(pairs))

