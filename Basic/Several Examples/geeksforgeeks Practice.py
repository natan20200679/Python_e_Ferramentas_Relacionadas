"""# Missing And Repeating"""

def findTwoElement(arr):
  n = len(arr)
  arr.sort()
  res = []

  for i in range(n - 1):
    if arr[i] == arr[i + 1]:
      res.append(arr[i])

  for i in range(1, n):
    if i not in arr:
      res.append(i)
      break

  return res

findTwoElement([4, 3, 6, 2, 1, 1, 4, 7, 12])

"""# Floor In A Sorted Array"""

def findFloor(arr, x):
    low = 0
    high = len(arr) - 1
    i_floor = -1

    while (low <= high):
        mid = (low + high) // 2

        if arr[mid] <= x:
            i_floor = mid
            low = mid + 1
        else:
            high = mid - 1

    return i_floor

findFloor([1, 2, 8, 10, 10, 12, 19], 11)

"""# Count Digits"""

def evenlyDivides(n):
  s = str(n)
  digits = []

  for char in s:
    digit = int(char)
    digits.append(digit)

  q = 0

  for digit in digits:
    if digit != 0 and n % digit == 0:
      q += 1

  return q

