"""# Array Leaders"""

def leaders(arr):
  res = []
  max_from_right = -1

  for i in range(len(arr) - 1, -1, -1):
    if arr[i] >= max_from_right:
      res.append(arr[i])
      max_from_right = arr[i]

  res.reverse()
  print(res)

